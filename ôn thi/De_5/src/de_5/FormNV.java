    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        // TODO add your handling code here:
        if (tht.getText().equals("")||tns.getText().equals("")||tdc.getText().equals("")||ths.getText().equals("")||tdv.getText().equals("")|| (!rnam.isSelected()&&!rnu.isSelected())){
            JOptionPane.showMessageDialog(this, "Yeu cau nhap thong tin");
        }else{
            nv.setHoten(tht.getText());
            nv.setNgaySinh(tns.getText());
            nv.setDiaChi(tdc.getText());
            if(rnu.isSelected()){
                nv.setGioiTinh("Nu");
            }else{
                nv.setGioiTinh("Nam");
            }
            nv.setMaNV(tma.getText());
            nv.setHeso(Double.parseDouble(ths.getText()));
            nv.setDvi(tdv.getText());
            dsnv.add(nv);
            JOptionPane.showMessageDialog(this, "Them thanh cong");
            System.out.println(nv.toString());
            luufile();
        }
    }//GEN-LAST:event_addActionPerformed

    public void luufile(){
        try {
            FileOutputStream fos=new FileOutputStream("nhanvien.txt");
            OutputStreamWriter osw=new OutputStreamWriter(fos);
            BufferedWriter bw=new BufferedWriter(osw);
            String line="";
            for(NhanVien ds:dsnv){
                line+=ds.getHoten()+"$";
                line+=ds.getNgaySinh()+"$";
                line+=ds.getDiaChi()+"$";
                if(rnam.isSelected()){
                    line+="Nam"+"$";
                    
                }else{
                    line+="Nu"+"$";
                }
                line+=ds.getMaNV()+"$";
                line+=ds.getHeso()+"$";
                line+=ds.getDvi()+"$";
                bw.write(line);
                bw.newLine();
            }
            bw.close();
            osw.close();
            fos.close();
            JOptionPane.showMessageDialog(this, "Luu thanh cong");
        } catch (FileNotFoundException e) {
            JOptionPane.showMessageDialog(this, "Loi ghi file","ERROR",JOptionPane.ERROR_MESSAGE);
        } catch(IOException e){
            JOptionPane.showMessageDialog(this, "Loi doc file","ERROR",JOptionPane.ERROR_MESSAGE);
        }
    }
