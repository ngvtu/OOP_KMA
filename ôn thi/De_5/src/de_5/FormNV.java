    public void docfile(){
        try{
            FileInputStream fis = new FileInputStream("nhanvien.txt");
            InputStreamReader isr = new InputStreamReader(fis);
            BufferedReader br = new BufferedReader(isr);
            String line = "";
            while((line=br.readLine())!=null){
                String[] a=line.split("\\$");
                String ma = a[0];
                double hs=Double.parseDouble(a[1]);
                String dv = a[2];
                String ht= a[3];
                String ns=a[4];
                String dc=a[5];
                
                String gt=a[6];
                NhanVien nv = new NhanVien(ma,hs,dv,ht,ns,dc,gt);
                dsdoc.add(nv);
                System.out.println(nv.toString());
                
            }
            br.close();
            isr.close();
            fis.close();
            JOptionPane.showMessageDialog(this, "Doc thanh cong");
        }catch(IOException ex){
            JOptionPane.showMessageDialog(this, "Loi doc file", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
        
    }
    public void doctable(){
        try {
            FileInputStream fis=new FileInputStream("nhanvien.txt");
            InputStreamReader isr=new InputStreamReader(fis);
            BufferedReader br=new BufferedReader(isr);
            String line="";
            while((line=br.readLine())!=null){
                String[] row=line.split("\\$");
                model.addRow(row);
            }
            br.close();
            isr.close();
            fis.close();
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Loi doc file", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }
