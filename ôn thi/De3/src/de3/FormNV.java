    public void doctable(){
        
        
        try {
            bw.close();
            osw.close();
            fos.close();
            FileInputStream fis=new FileInputStream("nhanvien.dat");
            InputStreamReader isr=new InputStreamReader(fis);
            BufferedReader br=new BufferedReader(isr);
            String Line="";
            while((Line=br.readLine())!=null){
                String[] row=Line.split("\\$");
                model.addRow(row);
            }
            br.close();
            isr.close();
            fis.close();
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Loi doc file","ERROR",JOptionPane.ERROR_MESSAGE);
        }
    }
    public void ghiFile(){
        try {
                    
            String line="";
            for(int i=0;i<list.size();i++)
                line+="";
                line+=nv.getHoTen()+"$";
                line+=nv.getNgaySinh()+"$";
                line+=nv.getDiaChi()+"$";
                if(rnam.isSelected()){
                    line+="Nam"+"$";
                }else{
                    line+="Nu"+"$";
                }
                line+=nv.getPhongBan()+"$";
                line+=nv.getHsl()+"$";
                line+=nv.getThamNien()+"$";
                line+=nv.getLuongcb()+"$";
                
                line+=nv.luongthuc()+"$";
                bw.write(line);
                bw.newLine();
            System.out.println(line);
            
            JOptionPane.showMessageDialog(this, "Luu thanh cong");
            
        } catch (FileNotFoundException e) {
            JOptionPane.showMessageDialog(this, "Loi tao file");
        } catch(IOException e){
            JOptionPane.showMessageDialog(this, "Loi ghi file");
        }
        
    }
