    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        // TODO add your handling code here:
        if(tht.getText().equals("")||tns.getText().equals("")||tdc.getText().equals("")||tpb.getText().equals("")||ths.getText().equals("")||ttn.getText().equals("")||tcb.getText().equals("")||(!rnam.isSelected()&&!rnu.isSelected())){
            JOptionPane.showMessageDialog(this, "Yeu cau nhap thong tin");
        }
        else{
            nv.setHoTen(tht.getText());
            nv.setNgaySinh(tns.getText());
            nv.setDiaChi(tdc.getText());
            nv.setPhongBan(tpb.getText());
            if(rnam.isSelected()){
                nv.setGioiTinh("Nam");
            }else{
                nv.setGioiTinh("Nu");
            }
            nv.setHsl(Integer.parseInt(ths.getText()));
            nv.setThamNien(Integer.parseInt(ttn.getText()));
            nv.setLuongcb(Float.parseFloat(tcb.getText()));
            list.add(nv);
            System.out.println(nv.toString());
            JOptionPane.showMessageDialog(this, "them thanh cong");
            ghiFile();
        }
    }//GEN-LAST:event_addActionPerformed

