    public void ghiFile(){
        try {
            FileOutputStream fos=new FileOutputStream("sinhvienAT.txt");
            OutputStreamWriter osw=new OutputStreamWriter(fos);
            BufferedWriter bw=new BufferedWriter(osw);
            String line="";
            for(SinhVienATTT attt:listAT){
                line+=attt.getMaSV();
                line+=attt.getHoTen();
                line+=attt.getNgaySinh();
                line+=attt.getDiemTB();
                line+=attt.getHocPhi();
                if(rnam.isSelected()){
                    line+="Nam";
                }else{
                    line+="Nu";
                }
                bw.write(line);
                bw.newLine();
                
            }
            JOptionPane.showMessageDialog(this, "luu thanh cong");
            bw.close();
            osw.close();
            fos.close();
            
        } catch (Exception e) {
        }
        
        try {
            FileOutputStream fos=new FileOutputStream("sinhvienMM.txt");
            OutputStreamWriter osw=new OutputStreamWriter(fos);
            BufferedWriter bw=new BufferedWriter(osw);
            String line="";
            for(SinhVienMM svmm:listMM){
                line+=svmm.getMaSV();
                line+=svmm.getHoTen();
                line+=svmm.getNgaySinh();
                line+=svmm.getDiemTB();
                line+=svmm.getDvi();
                line+=svmm.getLuong();
                if(rnam.isSelected()){
                    line+="Nam";
                }else{
                    line+="Nu";
                }
                bw.write(line);
                bw.newLine();
            }
            JOptionPane.showMessageDialog(this, "luu thanh cong");
            bw.close();
            osw.close();
            fos.close();
        } catch (Exception e) {
        }
    }
