    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        // TODO add your handling code here:
        if(!rat.isSelected() && !rmm.isSelected()){
            JOptionPane.showMessageDialog(this, "Vui long chon loai SV","ERROR",JOptionPane.ERROR_MESSAGE);
        }else{
            if(rat.isSelected()){
                if(kiemtraAT()==false){
                    at.setMaSV(tma.getText());
                    at.setHoTen(tht.getText());
                    at.setNgaySinh(tns.getText());
                    at.setDiemTB(Float.parseFloat(tdiem.getText()));
                    at.setHocPhi(Float.parseFloat(thp.getText()));
                    if(rnam.isSelected()){
                        at.setGioiTinh("Nam");
                    }
                    else{
                        at.setGioiTinh("Nu");
                    }
                    listAT.add(at);
                    System.out.println(at.toString());
                    JOptionPane.showMessageDialog(this, "Them thanh cong");
                }
                else{
                    JOptionPane.showMessageDialog(this, "Vui long nhap day du thong tin","ERROR",JOptionPane.ERROR_MESSAGE);
                    }
            }
            else if(rmm.isSelected()){
                if(kiemtraMM()==false){
                    mm.setMaSV(tma.getText());
                    mm.setHoTen(tht.getText());
                    mm.setNgaySinh(tns.getText());
                    mm.setDvi(tdv.getText());
                    mm.setDiemTB(Float.parseFloat(tdiem.getText()));
                    mm.setLuong(Float.parseFloat(tluong.getText()));
                    if(rnam.isSelected()){
                        at.setGioiTinh("Nam");
                    }
                    else{
                        at.setGioiTinh("Nu");
                    }
                    listMM.add(mm);
                    System.out.println(mm.toString());
                    JOptionPane.showMessageDialog(this, "Them thanh cong");
                }
                else{
                    JOptionPane.showMessageDialog(this, "Vui long nhap day du thong tin","ERROR",JOptionPane.ERROR_MESSAGE);
                    }
            }
        }
    }//GEN-LAST:event_addActionPerformed

